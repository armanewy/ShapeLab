#![forbid(unsafe_code)]

//! Deterministic game-asset-pack export scaffolding.
//!
//! These types describe package sidecars and readiness evidence without taking
//! a dependency on any one runtime, DCC, or mesh export backend.

use std::collections::BTreeSet;
use std::fs;
use std::path::{Component, Path};

use serde::{Deserialize, Serialize};

use crate::surface::{SurfaceArtifact, validate_surface_artifact_with_root};
use crate::{CollisionProxy, GameAssetValidationIssue};

/// Current schema version for static-prop game-ready package manifests.
pub const STATIC_PROP_GAME_READY_PACKAGE_SCHEMA_VERSION: u32 = 1;

/// Current schema version for static-prop readiness validation reports.
pub const STATIC_PROP_READINESS_REPORT_SCHEMA_VERSION: u32 = 1;

/// Runtime-neutral manifest for one static-prop game-readiness package.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct StaticPropGameReadyPackage {
    /// Package schema version.
    pub schema_version: u32,
    /// Source Visual Foundry profile slug.
    pub profile_id: String,
    /// Human-facing asset name.
    pub display_name: String,
    /// Human-facing asset family.
    pub asset_family: String,
    /// Stable source recipe hash from the compiled artifact.
    pub source_recipe_hash: u64,
    /// Foundry artifact fingerprint, when generated by Foundry.
    pub artifact_fingerprint: String,
    /// Frozen exact mesh and package artifacts.
    pub frozen_mesh: FrozenMeshArtifact,
    /// Deterministic LOD chain policy and artifact references.
    pub lod_policy: StaticPropLodPolicy,
    /// Material slot assignment policy.
    pub material_slots: Vec<MaterialSlotAssignment>,
    /// UV readiness policy.
    pub uv_policy: UvPolicy,
    /// Concrete surface artifact reference, when emitted.
    #[serde(default)]
    pub surface_artifact: Option<String>,
    /// Collision proxy evidence.
    pub collision: StaticPropCollision,
    /// DCC/runtime handoff evidence.
    pub handoff: StaticPropHandoff,
    /// Visual evidence artifact references.
    pub visual_evidence: StaticPropVisualEvidence,
    /// Manual review marker.
    pub manual_review: ManualReviewMarker,
}

/// Exact frozen model artifacts produced for the package.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct FrozenMeshArtifact {
    /// Canonical model-package directory.
    pub canonical_model_package: String,
    /// Canonical asset manifest inside the model package.
    pub asset_manifest: String,
    /// Model-package verification sidecar.
    pub package_verification: String,
    /// Exact grouped OBJ sidecar.
    pub grouped_obj: String,
    /// Blender reconstruction script inside the model package.
    pub blender_reconstruct_script: String,
    /// Whether compile validation passed before packaging.
    pub compile_validation_passed: bool,
    /// Whether model validation passed before packaging.
    pub model_validation_passed: bool,
}

/// LOD policy and emitted artifacts for a static prop.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct StaticPropLodPolicy {
    /// Human-facing policy name.
    pub policy: String,
    /// LOD levels in increasing LOD index order.
    pub levels: Vec<StaticPropLodLevel>,
}

/// One static-prop LOD level.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct StaticPropLodLevel {
    /// Zero-based LOD index.
    pub index: u32,
    /// Stable level identifier.
    pub id: String,
    /// Source used to create this level.
    pub source: String,
    /// Artifact path for the level.
    pub artifact: String,
    /// Target triangle count for this level.
    pub target_triangle_count: u32,
    /// Whether this LOD is exact source geometry.
    pub exact_source_geometry: bool,
}

/// One material slot assignment.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct MaterialSlotAssignment {
    /// Stable material slot key.
    pub slot_id: String,
    /// Human-facing slot name.
    pub display_name: String,
    /// Semantic roles assigned to this slot.
    pub semantic_roles: Vec<String>,
    /// Plain-language policy. This is not a material graph.
    pub policy: String,
    /// Whether the slot contains a final material/texture payload.
    pub material_payload_ready: bool,
}

/// Readiness status for a feature or handoff.
#[derive(Debug, Default, Copy, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum StaticPropFeatureStatus {
    /// The feature exists and passed automated validation.
    Ready,
    /// The feature is represented by policy only.
    PolicyOnly,
    /// The feature is not implemented in this package.
    #[default]
    NotImplemented,
    /// Human review is required.
    ManualRequired,
}

/// UV readiness policy.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct UvPolicy {
    /// UV feature status.
    pub status: StaticPropFeatureStatus,
    /// Whether UVs are required for a full game-ready claim.
    pub required_for_game_ready: bool,
    /// Stable blocker code when UVs are unavailable.
    pub blocker_code: Option<String>,
    /// Plain-language explanation.
    pub explanation: String,
}


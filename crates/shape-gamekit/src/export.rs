fn validate_manual_review(
    builder: &mut StaticPropReadinessReportBuilder,
    marker: &ManualReviewMarker,
) {
    builder.require_non_empty(
        "manual_review.notes",
        &marker.notes,
        "empty_manual_review_notes",
    );
    match marker.status {
        ManualReviewStatus::Approved => {
            builder.require(
                "manual_review_reviewer_present",
                marker
                    .reviewer
                    .as_deref()
                    .is_some_and(|reviewer| !reviewer.trim().is_empty()),
                Some("manual_review.reviewer"),
                "missing_manual_reviewer",
                "Manual review includes a reviewer.",
                "Approved manual review must include a reviewer.",
            );
        }
        ManualReviewStatus::Pending => builder.block(
            Some("manual_review.status"),
            "manual_review_pending",
            "Manual game-readiness review has not been completed.",
        ),
        ManualReviewStatus::Rejected => builder.block(
            Some("manual_review.status"),
            "manual_review_rejected",
            "Manual game-readiness review rejected the package.",
        ),
    }
}

fn array_is_finite<const N: usize>(values: &[f32; N]) -> bool {
    values.iter().all(|value| value.is_finite())
}

fn array_is_positive<const N: usize>(values: &[f32; N]) -> bool {
    values.iter().all(|value| value.is_finite() && *value > 0.0)
}

struct StaticPropReadinessReportBuilder {
    profile_id: String,
    checks: Vec<StaticPropReadinessCheck>,
    blockers: Vec<GameAssetValidationIssue>,
    warnings: Vec<GameAssetValidationIssue>,
}

impl StaticPropReadinessReportBuilder {
    fn new(profile_id: &str) -> Self {
        Self {
            profile_id: profile_id.to_owned(),
            checks: Vec::new(),
            blockers: Vec::new(),
            warnings: Vec::new(),
        }
    }

    fn finish(self) -> StaticPropReadinessReport {
        let status = if self.blockers.is_empty() {
            StaticPropReadinessStatus::Ready
        } else {
            StaticPropReadinessStatus::Blocked
        };
        StaticPropReadinessReport {
            schema_version: STATIC_PROP_READINESS_REPORT_SCHEMA_VERSION,
            profile_id: self.profile_id,
            status,
            checks: self.checks,
            blockers: self.blockers,
            warnings: self.warnings,
        }
    }

    fn require(
        &mut self,
        code: impl Into<String>,
        condition: bool,
        subject: Option<impl Into<String>>,
        issue_code: impl Into<String>,
        pass_message: impl Into<String>,
        fail_message: impl Into<String>,
    ) {
        let code = code.into();
        if condition {
            self.checks.push(StaticPropReadinessCheck {
                code,
                passed: true,
                message: pass_message.into(),
            });
        } else {
            let message = fail_message.into();
            self.checks.push(StaticPropReadinessCheck {
                code,
                passed: false,
                message: message.clone(),
            });
            self.blockers.push(GameAssetValidationIssue {
                subject: subject.map(Into::into),
                code: issue_code.into(),
                message,
            });
        }
    }

    fn require_non_empty(
        &mut self,
        subject: impl Into<String>,
        value: &str,
        issue_code: impl Into<String>,
    ) {
        let subject = subject.into();
        let check_code = format!("{}_present", subject.replace('.', "_"));
        self.require(
            check_code,
            !value.trim().is_empty(),
            Some(subject.clone()),
            issue_code,
            format!("{subject} is present."),
            format!("{subject} must be present."),
        );
    }

    fn pass(&mut self, code: impl Into<String>, message: impl Into<String>) {
        self.checks.push(StaticPropReadinessCheck {
            code: code.into(),
            passed: true,
            message: message.into(),
        });
    }

    fn block(
        &mut self,
        subject: Option<impl Into<String>>,
        code: impl Into<String>,
        message: impl Into<String>,
    ) {
        let code = code.into();
        let message = message.into();
        self.checks.push(StaticPropReadinessCheck {
            code: code.clone(),
            passed: false,
            message: message.clone(),
        });
        self.blockers.push(GameAssetValidationIssue {
            subject: subject.map(Into::into),
            code,
            message,
        });
    }

    fn warn(
        &mut self,
        subject: Option<impl Into<String>>,
        code: impl Into<String>,
        message: impl Into<String>,
    ) {
        self.warnings.push(GameAssetValidationIssue {
            subject: subject.map(Into::into),
            code: code.into(),
            message: message.into(),
        });
    }
}

fn validate_collision(
    builder: &mut StaticPropReadinessReportBuilder,
    collision: &StaticPropCollision,
) {
    builder.require_non_empty(
        "collision.source",
        &collision.source,
        "empty_collision_source",
    );
    builder.require(
        "collision_proxy_present",
        !collision.proxies.is_empty(),
        Some("collision.proxies"),
        "missing_collision_proxy",
        "At least one collision proxy is present.",
        "A static prop package must include at least one collision proxy.",
    );
    for (index, proxy) in collision.proxies.iter().enumerate() {
        let valid = match proxy {
            CollisionProxy::Box {
                center,
                half_extents,
            } => array_is_finite(center) && array_is_positive(half_extents),
            CollisionProxy::Capsule { a, b, radius } => {
                array_is_finite(a) && array_is_finite(b) && radius.is_finite() && *radius > 0.0
            }
            CollisionProxy::Cylinder {
                center,
                radius,
                height,
            } => {
                array_is_finite(center)
                    && radius.is_finite()
                    && *radius > 0.0
                    && height.is_finite()
                    && *height > 0.0
            }
            CollisionProxy::ConvexHullReserved { reason } => !reason.trim().is_empty(),
        };
        builder.require(
            format!("collision_proxy_{index}_valid"),
            valid,
            Some(format!("collision.proxies.{index}")),
            "invalid_collision_proxy",
            "Collision proxy is finite and non-degenerate.",
            "Collision proxies must be finite and non-degenerate.",
        );
    }
}

fn validate_handoff(builder: &mut StaticPropReadinessReportBuilder, handoff: &StaticPropHandoff) {
    builder.require_non_empty(
        "handoff.primary_package_format",
        &handoff.primary_package_format,
        "empty_primary_package_format",
    );
    builder.require_non_empty(
        "handoff.blender_handoff_script",
        &handoff.blender_handoff_script,
        "missing_blender_handoff_script",
    );
    builder.require(
        "blender_handoff_ready",
        handoff.blender_status == StaticPropFeatureStatus::Ready,
        Some("handoff.blender_status"),
        "blender_handoff_not_ready",
        "Blender handoff script is present.",
        "Blender handoff must be available for this package.",
    );
    if handoff.glb_status != StaticPropFeatureStatus::Ready {
        builder.block(
            Some("handoff.glb_status"),
            handoff
                .glb_blocker_code
                .as_deref()
                .unwrap_or("glb_handoff_not_ready"),
            "Direct GLB handoff is not implemented for this package.",
        );
    } else if handoff
        .glb_artifact
        .as_deref()
        .unwrap_or("")
        .trim()
        .is_empty()
    {
        builder.block(
            Some("handoff.glb_artifact"),
            "missing_glb_artifact",
            "GLB handoff is marked ready but no GLB artifact is referenced.",
        );
    } else {
        builder.pass("glb_handoff_ready", "GLB handoff artifact is present.");
    }
    if handoff.engine_import_status != StaticPropFeatureStatus::Ready {
        builder.block(
            Some("handoff.engine_import_status"),
            "engine_import_proof_missing",
            "DCC/runtime engine import proof is missing.",
        );
    } else if handoff
        .engine_import_proof
        .as_deref()
        .unwrap_or("")
        .trim()
        .is_empty()
    {
        builder.block(
            Some("handoff.engine_import_proof"),
            "engine_import_proof_missing",
            "Engine import proof is marked ready but no proof artifact is referenced.",
        );
    } else {
        builder.pass(
            "engine_import_proof_ready",
            "DCC/runtime import proof is present.",
        );
    }
    if handoff.engine_native_package_status != StaticPropFeatureStatus::Ready {
        builder.block(
            Some("handoff.engine_native_package_status"),
            "engine_native_package_not_implemented",
            "Unity, Unreal, Godot, or other engine-native packages are not implemented.",
        );
    }
}

